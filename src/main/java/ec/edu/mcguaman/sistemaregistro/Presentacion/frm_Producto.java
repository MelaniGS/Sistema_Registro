    private void mostrarDatos(List<Producto> lista) {
        modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("CÃ³digo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Precio");

        for (Producto prod : lista) {
            Object[] fila = {
                prod.getIdP(),
                prod.getCodigo(),
                prod.getNombre(),
                prod.getPrecio()
            };
            modelo.addRow(fila);
        }
        tbl_producto.setModel(modelo);
    }

